package com.icss.day01;

/**
 * 创建一个Hello类，在Hello类中创建主方法，
 * 在主方法内部int声明两个变量
 * 变量名为a，b赋值分别为80,90
 * 输出两个值，并且求和输出
 */
public class Hello {
    public static void main(String[] args) {
        int a, b, c;
        a = 80;
        b = 90;
        c = a + b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println("今天第一天学习Java！");
    }
}
